public class DataModel {
	
	private String name;
    private String id;
    private String article;
    private String priceDiler;
    private String priceRozn;
    private String ost;
    
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getArticle() {
		return article;
	}
	public void setArticle(String article) {
		this.article = article;
	}
	public String getPriceDiler() {
		return priceDiler;
	}
	public void setPriceDiler(String priceDiler) {
		this.priceDiler = priceDiler;
	}
	public String getPriceRozn() {
		return priceRozn;
	}
	public void setPriceRozn(String priceRozn) {
		this.priceRozn = priceRozn;
	}
	public String getOst() {
		return ost;
	}
	public void setOst(String ost) {
		this.ost = ost;
	}

    

}
